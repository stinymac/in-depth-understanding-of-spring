package org.mac.sf.ioc.dependency;

/**
 * @auther mac
 * @date 2020-07-12 11:44
 */
public class PersonHolder {

    private Person person;

    public PersonHolder() {
    }

    public PersonHolder(Person person) {
        this.person = person;
    }

    public Person getPerson() {
        return person;
    }

    public void setPerson(Person person) {
        this.person = person;
    }

    @Override
    public String toString() {
        return "PersonHolder{" +
                "person=" + person +
                '}';
    }
}
