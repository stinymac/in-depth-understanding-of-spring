package org.mac.sf.bean;

/**
 * @auther mac
 * @date 2020-07-07 19:43
 */
public class Person {

    private String name;
    private int age;

    public static Person createPerson() {
        return new Person();
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    @Override
    public String toString() {
        return "Person{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
